package Alpha.Variable_data_types;

public class Average {
    /*
     * In a program,input 3 numbers:A,B and C.You have to output the average of
     * these 3 numbers
     */
    public static void main(String[] args) {
        int a = 22;
        int b = 45;
        int c = 85;
        float avg = (a + b + c) / 3;
        System.out.println("Average is : " + avg);
    }

}
